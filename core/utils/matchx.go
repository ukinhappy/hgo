package utils

// MinOfInt min of int
func MinOfInt(a, b int) int {
	if a < b {
		return a
	}
	return b
}

// MaxOfInt max of int
func MaxOfInt(a, b int) int {
	if a > b {
		return a
	}
	return b
}

// MinOfInt min of int
func MinOfUInt32(a, b uint32) uint32 {
	if a < b {
		return a
	}
	return b
}

// MaxOfInt max of int
func MaxOfUInt32(a, b uint32) uint32 {
	if a > b {
		return a
	}
	return b
}
